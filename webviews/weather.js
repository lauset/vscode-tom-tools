function handleData(resp) {
  if (resp.now) {
    handleNow(resp.now)
  }
  if (resp.seven) {
    handleSeven(resp.seven)
  }
  if (resp.yq163) {
    handleYQ163(resp.yq163)
  }
  setTimeout(() => {
    const loader = document.getElementById('loader-outer')
    loader.style.display = 'none'
  }, 500)
}

function initData() {
  callVscode({ cmd: 'vsGetWeaData' }, (resp) => {
    handleData(resp)
  })
}

function getSevenOptions({ maxTemp, minTemp, labelTemp }) {
  const commonOptions = {
    borderJoinStyle: 'round',
    cubicInterpolationMode: 'monotone',
    pointStyle: 'rectRot',
    pointRadius: 3,
    pointHoverRadius: 9
  }
  const data = {
    labels: labelTemp,
    datasets: [
      {
        label: 'Max',
        data: maxTemp,
        borderColor: '#ff6384',
        backgroundColor: '#ffb0c1',
        ...commonOptions
      },
      {
        label: 'Min',
        data: minTemp,
        borderColor: '#36a2eb',
        backgroundColor: '#9ad0f5',
        ...commonOptions
      }
    ]
  }
  const config = {
    type: 'line',
    data,
    options: {
      responsive: true,
      plugins: {
        legend: {
          position: 'top'
        },
        title: {
          display: true,
          text: '七日温度'
        }
      }
    }
  }
  return config
}

